/*
    try-catch: we can simply say that they allow us to  handle exceptions 
        (errors) that occur during code execution.
    
    How they work:
    - try block: Contains code that might throw an error.
    - catch block: Executes if an error is thrown in the try block. It receives the error object.

    Their importance:
    - Prevents crashes: Without error handling, an error can crash our app.
    - Improves UX: We can show user-friendly messages instead of raw errors.
    - Debugging.

*/